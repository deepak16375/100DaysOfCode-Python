# Display a welcoming message for the user
print("Welcome to the Band Name Generator")

# Ask the user to input the name of the city they grew up in
city_name = input("What is the name of the city you grew up in?\n")

# Ask the user to input the name of their pet
pet_name = input("What is the name of your pet?\n")

# Combine the city name and pet name to create a band name
band_name = city_name + " " + pet_name

# Display the generated band name to the user
print("Your band name could be " + band_name)
